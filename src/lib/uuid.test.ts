import { describe, it, expect } from 'vitest';
import { generateUUID } from './uuid';

describe('generateUUID', () => {
  it('should generate a valid UUID v4 format', () => {
    const uuid = generateUUID();
    
    // UUID v4 format: xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx
    const uuidRegex = /^[0-9a-f]{8}-[0-9a-f]{4}-4[0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i;
    expect(uuid).toMatch(uuidRegex);
  });

  it('should generate unique UUIDs', () => {
    const uuid1 = generateUUID();
    const uuid2 = generateUUID();
    const uuid3 = generateUUID();
    
    expect(uuid1).not.toBe(uuid2);
    expect(uuid2).not.toBe(uuid3);
    expect(uuid1).not.toBe(uuid3);
  });

  it('should generate UUIDs with version 4', () => {
    const uuid = generateUUID();
    const versionChar = uuid.charAt(14); // 15th character (0-indexed 14) should be '4'
    
    expect(versionChar).toBe('4');
  });

  it('should generate UUIDs with correct variant', () => {
    const uuid = generateUUID();
    const variantChar = uuid.charAt(19); // 20th character should be 8, 9, a, or b
    
    expect(['8', '9', 'a', 'b', 'A', 'B']).toContain(variantChar);
  });

  it('should generate 100 valid UUIDs', () => {
    const uuids = new Set<string>();
    const uuidRegex = /^[0-9a-f]{8}-[0-9a-f]{4}-4[0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i;
    
    for (let i = 0; i < 100; i++) {
      const uuid = generateUUID();
      expect(uuid).toMatch(uuidRegex);
      uuids.add(uuid);
    }
    
    // All UUIDs should be unique
    expect(uuids.size).toBe(100);
  });
});
